package org.systemsbiology.xtandem.mzml;

import org.proteios.io.*;

/**
 * org.systemsbiology.xtandem.mzml.ExtendedSpectrumImpl
 * Added an Id to a spectrum
 * User: Steve
 * Date: Apr 26, 2011
 */
public class ExtendedSpectrumImpl extends SpectrumImpl 
{
    public static final ExtendedSpectrumImpl[] EMPTY_ARRAY = {};

    private String m_Id;
    private int m_ScanNumber;
    private int m_MSLevel;

    public ExtendedSpectrumImpl() {
    }

    public String getId() {
        return m_Id;
    }

    public void setId(final String pId) {
        m_Id = pId;
    }

    public int getScanNumber() {
        return m_ScanNumber;
    }

    public void setScanNumber(final int pScanNumber) {
        m_ScanNumber = pScanNumber;
    }

    public int getMSLevel() {
        return m_MSLevel;
    }

    public void setMSLevel(final int MSLevel) {
        m_MSLevel = MSLevel;
    }
}
